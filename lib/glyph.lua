function glyph57(glyph, x, y)
  local ptable = glyphs_57[glyph]
  if ptable then
    local pi = 1
    for yi = 0, 6 do
      local ypos = y + yi
      for xi = 0, 4 do
        local xpos = x + xi
        if ptable[pi] == 1 then
          screen.pixel(xpos, ypos)
        end
        pi = pi + 1
      end
    end
  end
end

