    local glyph = string.upper(c)
    glyph57(glyph, x - 6 + i * 6, y)
  end
end

function glyph_text(input, x, y)
  str = tostring(input)
  for i = 1, #str do
    local c = str:sub(i, i)
    local glyph = string.upper(c)
    glyph35(glyph, x - 4 + i * 4, y)
  end
end

function glf(g, x, y)
  if glfs[g] then
    for yi = 1, #glfs[g] do
      local ypos = y + yi
      for xi = 1, #glfs[g][yi] do
        local xpos = x + xi
        if glfs[g][yi][xi] == 1 then
          screen.pixel(xpos, ypos)
        end
      end
    end
    return #glfs[g][1], #glfs[g]
  else
    return 0, 0
  end
end

function draw(t, x, y)
  for gi, row in ipairs(t) do
    local ypos = y + (gi - 1)
    local xpos = 0
    for ci, pixel in ipairs(row) do
      xpos = x + (ci - 1)
      if pixel == 1 then
        screen.pixel(xpos, ypos)
      end
    end
  end
end

